using Microsoft.Rest;
using System;
using System.Collections.Generic;
using System.Threading;
using System.Threading.Tasks;

namespace MeyerCorp.Square.V1.Item
{
    public class InventoryOperations : Operations, IInventoryOperations
    {
        /// <summary>
        /// Initializes a new instance of the OrdersOperations class.
        /// </summary>
        /// <param name='client'>
        /// Reference to the service client.
        /// </param>
        public InventoryOperations(Client client) : base(client) { }

        /// <summary>
        /// 
        /// </summary>
        /// <param name="values"></param>
        /// <returns>https://connect.squareup.com/v1/{{location_id}}/payments</returns>
        protected override Uri GetUri(params string[] values)
        {
            switch (values.Length)
            {
                case 1: return BaseUri.Append(values[0], "inventory");
                default: throw new ArgumentException();
            }
        }

        public Task<HttpOperationResponse<IList<InventoryEntry>>> GetWithHttpMessagesAsync(string locationId,
            short? limit,
            Dictionary<string, List<string>> customHeaders = null,
            CancellationToken cancellationToken = default(CancellationToken))
        {
            var uri = GetUri(locationId)
                .AppendOrderOrLimit(limit, null);

            return GetWithHttpMessagesAsync<IList<InventoryEntry>>(uri, customHeaders, cancellationToken);
        }
    }
}